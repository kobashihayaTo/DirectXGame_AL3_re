void EndScene::Initialize()
{
	dxCommon_ = DirectXCommon::GetInstance();
	input_ = Input::GetInstance();
	debugText_ = DebugText::GetInstance();
	nextScene_ = Scene::TITLE;
}

void EndScene::Update()
{
	if (input_->TriggerKey(DIK_W)) {
		endFlag_ = true;
		nextScene_ = Scene::TITLE;
	}
}

